/**
Refine the function getCharCount so that it returns the count of characters present in the str parameter it receives.
 */

function getCharCount(str) {
  return str.length;
}

// Sample usage - do not modify
console.log(getCharCount('Sam')); // 3
console.log(getCharCount('Alex 123')); // 8
console.log(getCharCount('Charley is here')); // 15
